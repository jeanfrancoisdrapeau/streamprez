# coding=utf-8
import re


def lol_trick(data):
    """
    Removes LOL's logo from data.

    |-------|               |---------------|      |-------|
    |       |               |               |      |       |
    |       |               |               |      |       |
    |       |               |               |      |       |
    |       |               |               |      |       |
    |       |               |     |---|     |      |       |
    |       |               |     |   |     |      |       |
    |       |________       |     |   |     |      |       |________
    |                |      |     |---|     |      |                |
    |                |      |               |      |                |
    |                |      |               |      |                |
    |                |      |               |      |                |
    |________________|      |_______________|      |________________|
    <o>   o    o>                \o/               <o    o   \o
    |   <|>  <|                  |                 \    X    |.
    /<   /<   /<                 / \                >\   >\   >\
    """

    # Ugly re!

    lol_logo = re.compile(
        "\s+\\|-------\\|               \\|---------------\\|      \\|-------\\|(.*)(\r*\n)" +
        "(\s*\\|       \\|               \\|               \\|      \\|       \\|(.*)(\r*\n))+" +
        "\s*\\|       \\|               \\|     \\|---\\|     \\|      \\|       \\|(.*)(\r*\n)" +
        "\s*\\|       \\|               \\|     \\|   \\|     \\|      \\|       \\|(.*)(\r*\n)" +
        "\s*\\|       \\|________       \\|     \\|   \\|     \\|      \\|       \\|________(.*)(\r*\n)" +
        "\s*\\|                \\|      \\|     \\|---\\|     \\|      \\|                \\|(.*)(\r*\n)" +
        "(\s*\\|                \\|      \\|               \\|      \\|                \\|(.*)(\r*\n))+" +
        "\s*\\|________________\\|      \\|_______________\\|      \\|________________\\|(.*)(\r*\n)" +
        "\s*<o>   o    o>                \\\o/               <o    o   \\\o(.*)(\r*\n)" +
        "\s*\\|   <\\|>  <\\|                  \\|                 \\\    X    \\|\\.(.*)(\r*\n)" +
        "\s*/<   /<   /<                 / \\\                >\\\   >\\\   >\\\(.*)(\r*\n)"
    )

    data = re.sub(lol_logo, '', data)

    return data
